    private void jMI_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMI_SaveActionPerformed
        // TODO add your handling code here:
        String thoughts = jTA_Thoughts.getText();
        System.out.println(thoughts);
        
        FileReader reader = null;
        try {
            reader = new FileReader("thoughts.txt");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String content = "";
        int i;
        try {
            while((i=reader.read()) !=-1) {
                content += (char) i;
            }
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            reader.close();
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        content += "Nowytekst";
        
        try {
            FileWriter writer = new FileWriter("thoughts.txt", true);
            writer.write(thoughts + "\n");
            writer.close();
            jTA_Thoughts.setText(thoughts + "\nSaved");
        } catch (Exception e) {
            jTA_Thoughts.setText(thoughts + "\nError");
        }
    }//GEN-LAST:event_jMI_SaveActionPerformed

